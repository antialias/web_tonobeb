<h1>How to make your move</h1>
<h2>to move a piece:</h2>
<p>Click on the piece you want to move. If you can move the piece you clicked on, it will assume a dashed border. Now, click on the square you want to move your piece to.</p>
<h2>To capture or kill a piece:</h2>
<p>If your pieces are in a position to capture or kill a piece, just click on your opponen's doomed piece. Clicking on the piece will cycle it through "kill", "capture", or "leave alone".</p>
<h2>To undo the move of a piece:</h2>
click a piece to undo all your moves up until the time you moved it. </p>
